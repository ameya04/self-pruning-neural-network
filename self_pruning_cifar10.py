import math
import numpy as np
import torch
import torch.nn as nn
import torch.nn.functional as F
import torch.optim as optim
import matplotlib.pyplot as plt
from torchvision import datasets, transforms
from torch.utils.data import DataLoader


class PrunableLinear(nn.Module):
    """
    A fully connected layer where each weight has a learnable gate in [0,1].
    gate_scores init to +3 so sigmoid(3) ≈ 0.95 — gates START open.
    Sparsity loss pushes scores negative → sigmoid → 0 → gate closes.
    """

    def __init__(self, in_features, out_features):
        super().__init__()

        self.in_features  = in_features
        self.out_features = out_features

        # Standard weight with Kaiming init (correct for ReLU)
        self.weight = nn.Parameter(torch.empty(out_features, in_features))
        nn.init.kaiming_uniform_(self.weight, a=math.sqrt(5))

        # Standard bias
        fan_in, _ = nn.init._calculate_fan_in_and_fan_out(self.weight)
        bound = 1 / math.sqrt(fan_in) if fan_in > 0 else 0
        self.bias = nn.Parameter(torch.empty(out_features))
        nn.init.uniform_(self.bias, -bound, bound)

        # Gate scores: init high so gates open, sparsity loss closes them
        self.gate_scores = nn.Parameter(
            torch.full((out_features, in_features), 3.0)
        )

    def forward(self, x):
        gates         = torch.sigmoid(self.gate_scores)
        pruned_weight = self.weight * gates
        return F.linear(x, pruned_weight, self.bias)

print("PrunableLinear defined.")

class PrunableNet(nn.Module):
    """
    Feed-forward network for CIFAR-10 using PrunableLinear layers.
    BatchNorm + Dropout improve baseline accuracy.
    """

    def __init__(self):
        super().__init__()

        self.fc1 = PrunableLinear(32 * 32 * 3, 1024)
        self.bn1 = nn.BatchNorm1d(1024)
        self.fc2 = PrunableLinear(1024, 512)
        self.bn2 = nn.BatchNorm1d(512)
        self.fc3 = PrunableLinear(512, 256)
        self.bn3 = nn.BatchNorm1d(256)
        self.fc4 = PrunableLinear(256, 10)
        self.dropout = nn.Dropout(0.3)

    def forward(self, x):
        x = x.view(x.size(0), -1)
        x = self.dropout(F.relu(self.bn1(self.fc1(x))))
        x = self.dropout(F.relu(self.bn2(self.fc2(x))))
        x = self.dropout(F.relu(self.bn3(self.fc3(x))))
        return self.fc4(x)

    def get_weight_params(self):
        """All parameters EXCEPT gate_scores (weights, biases, BN)."""
        gate_ids = {id(m.gate_scores) for m in self.modules()
                    if isinstance(m, PrunableLinear)}
        return [p for p in self.parameters() if id(p) not in gate_ids]

    def get_gate_params(self):
        """Only gate_scores parameters."""
        return [m.gate_scores for m in self.modules()
                if isinstance(m, PrunableLinear)]

    def sparsity_loss(self):
        """
        Normalized L1 on all sigmoid gates.
        Called with grad enabled so gate_scores receive gradients.
        """
        total, count = 0.0, 0
        for m in self.modules():
            if isinstance(m, PrunableLinear):
                g      = torch.sigmoid(m.gate_scores)
                total += g.sum()
                count += g.numel()
        return total / count

    def collect_all_gates(self):
        """Flat numpy array of all gate values. No grad — for analysis only."""
        parts = []
        for m in self.modules():
            if isinstance(m, PrunableLinear):
                with torch.no_grad():
                    parts.append(
                        torch.sigmoid(m.gate_scores).cpu().numpy().flatten()
                    )
        return np.concatenate(parts)

print("PrunableNet defined.")

transform = transforms.Compose([
    transforms.ToTensor(),
    transforms.Normalize(mean=(0.5, 0.5, 0.5), std=(0.5, 0.5, 0.5))
])

train_dataset = datasets.CIFAR10(root="./data", train=True,  download=True, transform=transform)
test_dataset  = datasets.CIFAR10(root="./data", train=False, download=True, transform=transform)

train_loader = DataLoader(train_dataset, batch_size=128, shuffle=True,  num_workers=2)
test_loader  = DataLoader(test_dataset,  batch_size=256, shuffle=False, num_workers=2)

print(f"Train batches: {len(train_loader)} | Test batches: {len(test_loader)}")

def train_model(lambda_sparse, epochs=20, verbose=True):
    """
    Two-optimizer training loop.

    Why two optimizers?
    - One optimizer causes classification gradient to fight sparsity gradient.
    - Gates reach equilibrium at sigmoid(0) = 0.5 and never move.
    - Separating them: weights only see classification loss,
      gates only see sparsity loss → no competition → gates actually prune.

    Args:
        lambda_sparse: sparsity penalty strength
        epochs:        training epochs
        verbose:       print per-epoch stats
    """

    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    if verbose:
        print(f"Device: {device}")

    model     = PrunableNet().to(device)
    criterion = nn.CrossEntropyLoss()

    # Optimizer 1: weights + biases + BN at normal lr
    weight_optimizer = optim.Adam(model.get_weight_params(), lr=1e-3)

    # Optimizer 2: gate_scores at high lr so sparsity signal dominates
    gate_optimizer   = optim.Adam(model.get_gate_params(), lr=5e-2)

    weight_scheduler = optim.lr_scheduler.StepLR(weight_optimizer, step_size=8, gamma=0.5)
    gate_scheduler   = optim.lr_scheduler.StepLR(gate_optimizer,   step_size=8, gamma=0.5)

    for epoch in range(epochs):
        model.train()
        total_cls    = 0.0
        total_sparse = 0.0
        n_batches    = len(train_loader)

        for images, labels in train_loader:
            images, labels = images.to(device), labels.to(device)

            # --- Step 1: Update weights using classification loss only ---
            weight_optimizer.zero_grad()
            gate_optimizer.zero_grad()
            outputs  = model(images)
            cls_loss = criterion(outputs, labels)
            cls_loss.backward()
            weight_optimizer.step()

            # --- Step 2: Update gates using sparsity loss only ---
            weight_optimizer.zero_grad()
            gate_optimizer.zero_grad()
            sparse_loss = model.sparsity_loss()
            (lambda_sparse * sparse_loss).backward()
            gate_optimizer.step()

            total_cls    += cls_loss.item()
            total_sparse += sparse_loss.item()

        weight_scheduler.step()
        gate_scheduler.step()

        if verbose:
            avg_cls    = total_cls    / n_batches
            avg_sparse = total_sparse / n_batches
            all_gates  = model.collect_all_gates()
            pct_pruned = (all_gates < 1e-2).mean() * 100
            print(
                f"Epoch [{epoch+1:>2}/{epochs}] "
                f"| Cls: {avg_cls:.4f} "
                f"| Sparse: {avg_sparse:.4f} "
                f"| Pruned: {pct_pruned:.1f}%"
            )

    model.to("cpu")
    return model

print("train_model defined.")

def evaluate_model(model):
    """
    Returns:
        accuracy  : fraction of correct test predictions
        sparsity  : % of gates below 0.01 (effectively pruned)
        all_gates : flat numpy array of all gate values
    """

    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    model.to(device)
    model.eval()

    correct = 0
    total   = 0

    with torch.no_grad():
        for images, labels in test_loader:
            images, labels = images.to(device), labels.to(device)
            outputs        = model(images)
            predictions    = outputs.argmax(dim=1)
            correct       += (predictions == labels).sum().item()
            total         += labels.size(0)

    accuracy  = correct / total
    all_gates = model.collect_all_gates()
    sparsity  = (all_gates < 1e-2).mean() * 100

    model.to("cpu")
    return accuracy, sparsity, all_gates

print("evaluate_model defined.")

